<?php

namespace ScrumBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * project_user
 *
 * @ORM\Table(name="project_user")
 * @ORM\Entity(repositoryClass="ScrumBundle\Repository\project_userRepository")
 */
class project_user
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var int
     *
     * @ORM\Column(name="id_user", type="integer")
     */
    private $idUser;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }



    /**
     * Set idUser
     *
     * @param integer $idUser
     *
     * @return project_user
     */
    public function setIdUser($idUser)
    {
        $this->idUser = $idUser;

        return $this;
    }

    /**
     * Get idUser
     *
     * @return int
     */
    public function getIdUser()
    {
        return $this->idUser;
    }
}

